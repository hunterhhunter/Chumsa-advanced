/**
 * ì •ì  íŒŒì¼ì„ Vault í”ŒëŸ¬ê·¸ì¸ í´ë”ë¡œ ë³µì‚¬
 */
async function copyStaticFiles() {
  const filesToCopy = [
    { src: "manifest.json", dest: "manifest.json", required: true },
    { src: "src/styles.css", dest: "styles.css", required: false }
  ];

  for (const file of filesToCopy) {
    try {
      const destPath = path.join(vaultPluginPath, file.dest);
      await fs.copyFile(file.src, destPath);
      console.log(`âœ… Copied: ${file.src} â†’ ${file.dest}`);
    } catch (error) {
      if (error.code === 'ENOENT') {
        if (file.required) {
          console.error(`âŒ Required file missing: ${file.src}`);
          process.exit(1);
        } else {
          console.warn(`âš ï¸  Optional file not found: ${file.src} (skipping)`);
        }
      } else {
        console.error(`âŒ Error copying ${file.src}:`, error.message);
        if (file.required) process.exit(1);
      }
    }
  }
}


